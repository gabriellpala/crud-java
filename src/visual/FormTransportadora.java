    public void atualizaTabela(){
        listTransportadora.clear();
        listTransportadora.addAll(dao.getLista());
        int linha = listTransportadora.size()-1;
        if(linha >= 0){
            tblTransportadora.setRowSelectionInterval(linha, linha);
            tblTransportadora.scrollRectToVisible
            (tblTransportadora.getCellRect(linha, linha, true));
                    
            
        }
    }
    
    private void trataEdicao( boolean editando){
        btnCancelar.setEnabled(editando);
        btnSalvar.setEnabled(editando);
        btnEditar.setEnabled(!editando);
        int linha = listTransportadora.size()-1;
        if(linha<0){
            btnExcluir.setEnabled(false);
            txtCodigo.setText("");
            txtNome.setText("");
        }else{
            btnExcluir.setEnabled(!editando);
        }
        btnNovo.setEnabled(!editando);
        btnFechar.setEnabled(!editando);
        btnPrimeiro.setEnabled(!editando);
        btnProximo.setEnabled(!editando);
        btnAnterior.setEnabled(!editando);
        btnUltimo.setEnabled(!editando);
        txtNome.setEnabled(editando);
        txtCidade.setEnabled(editando);
        txtCodigo.setEnabled(editando);
        txtEndereco.setEnabled(editando);
        txtNome.setEnabled(editando);
        txtUF.setEnabled(editando);
        tblTransportadora.setEnabled(editando);
    }
    
    public boolean validaCampos(){
        if(!(txtNome.getText().length()>0)){
            JOptionPane.showMessageDialog(null, "Informe o nome da transportadora");
            txtNome.requestFocus();
            return false;
        }
        
         if(!(txtEndereco.getText().length()>0)){
            JOptionPane.showMessageDialog(null, "Informe o endereço");
            txtEndereco.requestFocus();
            return false;
        }
         
          if(!(txtCidade.getText().length()>0)){
            JOptionPane.showMessageDialog(null, "Informe  cidade");
            txtCidade.requestFocus();
            return false;
        }
          
           if(!(txtUF.getText().length()>0)){
            JOptionPane.showMessageDialog(null, "Informe a UF");
            txtUF.requestFocus();
            return false;
        }
       
        return true;
        
    }

