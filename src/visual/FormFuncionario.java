    public void atualizaTabela(){
        listFuncionario.clear();
        listFuncionario.addAll(dao.getLista());
        int linha = listFuncionario.size()-1;
        if(linha >= 0){
            tblFuncionario.setRowSelectionInterval(linha, linha);
            tblFuncionario.scrollRectToVisible
            (tblFuncionario.getCellRect(linha, linha, true));
                    
            
        }
    }
    
    private void trataEdicao( boolean editando){
        btnCancelar.setEnabled(editando);
        btnSalvar.setEnabled(editando);
        btnEditar.setEnabled(!editando);
        int linha = listFuncionario.size()-1;
        if(linha<0){
            btnExcluir.setEnabled(false);
            txtCodigo.setText("");
            txtNome.setText("");
        }else{
            btnExcluir.setEnabled(!editando);
        }
        btnNovo.setEnabled(!editando);
        btnFechar.setEnabled(!editando);
        btnPrimeiro.setEnabled(!editando);
        btnProximo.setEnabled(!editando);
        btnAnterior.setEnabled(!editando);
        btnUltimo.setEnabled(!editando);
        
        txtNome.setEnabled(editando);
        txtRG.setEnabled(editando);
        txtCPF.setEnabled(editando);
        txtSalario.setEnabled(editando);
     
        cbxTransportadora.setEnabled(editando);
        jScrollPane1.setEnabled(editando);
    }
    
    public boolean validaCampos(){
        if(!(txtNome.getText().length()>0)){
            JOptionPane.showMessageDialog(null, "Informe o nome do funcionario");
            txtNome.requestFocus();
            return false;
        }
         if(!(txtCPF.getText().length()>0)){
            JOptionPane.showMessageDialog(null, "Informe o CPF");
            txtCPF.requestFocus();
            return false;
        }
        if(!(txtRG.getText().length()>0)){
            JOptionPane.showMessageDialog(null, "Informe o RG");
            txtRG.requestFocus();
            return false;
        }
        if(!(txtSalario.getText().length()>0)){
            JOptionPane.showMessageDialog(null, "Informe o salario");
            txtSalario.requestFocus();
            return false;
        }
            if((txtSalario.getText().length()>0)){
                try{
                    Double.parseDouble(txtSalario.getText());
                }catch(Exception e){
                     JOptionPane.showMessageDialog(null, "Informe somente números no salario");
            txtSalario.requestFocus();
            return false;
                }
           
        }
        if(!(cbxTransportadora.getSelectedIndex()>=0)){
            JOptionPane.showMessageDialog(null, "Informe a transportadora");
            cbxTransportadora.requestFocus();
            return false;
        }
      
            
           
        return true;
        
    }

